/*
5.- Escribe un programa de tres líneas que pida un número, 
pida otro número y escriba el resultado de sumar estos dos números.

Nota: Tener en cuenta la siguiente función: parseInt
https://developer.mozilla.org/es/docs/Web/JavaScript/Referencia/Objetos_globales/parseInt

*/

let a = parseInt(prompt("Ingresa un número"));
let b = parseInt(prompt("Ingresa otro número"));
 
document.write("La suma de los números da: " + (a + b));